# while loop
num = 5
while num <= 30:
    print(num)
    num += 1

num1 = 100
while num1 > 0:
    print(num1)
    num1 -= 10

num2 = 1
while num2 < 10:
    print(num2)
    if num2 == 10:
        break
    num2 += 1

num3 = 60
while num3 > 20:
    print(num3)
    if num3 == 5:
        break
    num3 -= 1
